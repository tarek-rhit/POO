package com.dai4;



public class Etudiant {

     String nom;
     String prenom;
     float note;

     public String getNom() {
          return nom;
     }

     public void setNom(String nom) {
          this.nom = nom;
     }

     public String getPrenom() {
          return prenom;
     }

     public void setPrenom(String prenom) {
          this.prenom = prenom;
     }

     public float getNote() {
          return note;
     }

     public void setNote(float note) {
          this.note = note;
     }
}
